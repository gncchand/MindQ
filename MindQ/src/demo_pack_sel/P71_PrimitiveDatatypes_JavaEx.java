package demo_pack_sel;

public class P71_PrimitiveDatatypes_JavaEx {

	public static void main(String[] args) {
		float x=10;
		System.out.println("x="+x);
		int y=(int) x;
		System.out.println("y="+y);
		float a=10.8f;
		System.out.println("a="+a);
		int b =(int) a;
		System.out.println("b="+b);
	}

}
